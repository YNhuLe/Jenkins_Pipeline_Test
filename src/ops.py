class OPS:
    @staticmethod
    def add(x, y):
        return x + y

    @staticmethod
    def subtract(x, y):
        return x - y

    @staticmethod
    def multiply(x, y):
        return x * y

    @staticmethod
    def divide(x, y):
        return x / y

    @staticmethod
    def cal(x, y, z):
        return (x + y) / z

    @staticmethod
    def percentage(x, y):
        return (x / y) * 100

    @staticmethod
    def plus(x, y):
        return (x + y + 2)
